<h2>About the Microdata Library</h2>
<p>The Microdata Library is the collection of microdata datasets produced by the World Bank as well as microdata datasets produced by partner agencies. It is a service established to broaden the use of survey datasets in line with the Banks philosophy of Open Data, Open Knowledge, and Open Solutions. Making survey data freely available to researchers is an important part of the democratization of development economics as envisioned by World Bank President Robert Zoellick.</p>
<p>The Microdata Library manages in all aspects of the microdata data curation lifecycle at the Bank. This includes the acquisition of data, documentation, cataloguing, preservation and dissemination.</p>
<h2>What do we mean by Microdata?</h2>
<p>Microdata are unit-level data obtained from sample surveys, censuses, and administrative systems. They allow in-depth understanding of socio-economic issues by studying relationships and interactions among phenomena. Microdata are thus key to designing projects and formulating policies, targeting interventions and monitoring and measuring the impact and results of projects, interventions and policies.</p>
<h2>What are our objectives?</h2>
<ul class="bullet-list">
<li>To promote equal access to and broad use of the Banks microdata, in order to foster diverse and high quality evidence based research work; </li>
<li>To increase the credibility of data and research work by allowing independent parties to replicate and/or challenge results; </li>
<li>To avoid duplication of data collection activities and increase the return on investment to research already completed by providing a central catalogue to researchers for all Bank collected microdata. </li>
<li>To reduce inconsistencies in analytical work by allowing all interested researchers to make use of the same datasets; </li>
<li>To increase the quality of data by allowing the community of researchers to provide feedback. </li>
<li>To preserve the data in accordance with international data standards. </li>
<li>Who are our target users? </li>
</ul>
<p>Microdata is mostly collected from sample household, establishments or facility surveys. Their analysis requires advanced understanding of sampling (and weighting techniques), and expertise in the use of statistical analysis packages such as Stata, SPSS or SAS. Its use is thus primarily for an audience of researchers and analysts. In many cases these data have substantive value for research and instruction, and are useful for the development of emerging research and statistical techniques.</p>
<h2>What topics are covered by our holdings?</h2>
<p>Our catalogue contains quantitative data collections covering a wide range of economic and social themes. We hold data from impact evaluation surveys, research datasets, large scale national surveys, ICS, and more.</p>
<h2>Where does our data come from?</h2>
<p>The World Bank collects and produces a diverse range of micro-datasets spanning all sectors within the Bank’s activities. The holdings include data produced by World Bank activities, national and other international agencies.</p>
<h2>What supporting information is available for our datasets?</h2>
<p>Our data catalogs provide survey reports, questionnaires, technical documents and detailed metadata compiled according to international standards. In addition a bibliography of publications using each dataset is being compiled and made available in our citation catalog.</p>
<h2>How are we involved in assisting member counties in curating their micordata?</h2>
<p>One of our key activities is technical support and capacity building: supporting data producers in client countries and partner agencies in adopting international good practices of curation and dissemination.</p>
<p>We do this by developing and providing free open source software solutions to the community as well by developing and providing documentation and training on good practice for data curation and dissemination.</p>
<p>Much of this work is conducted with the International Household Survey Network (IHSN) and its agencies as well as the Accelerated Data Program (ADP). These programs currently provide support activities in more than 70 countries and partner agencies through the ADP and IHSN.</p>