<h2>For data producers and curators</h2>

<dl>

<dt><a name="#">What are the benefits of having my dataset listed in the World Bank Microdata Library catalog?</a></dt>
<dd>
<p>The main benefit is the high visibility of the World Bank data site. The Data website is visited by millions of users. This will imply increased use of your data, more potential feedback from users and will often satisfy sponsors requirements that data be made accessible for secondary analysis.
</p>
</dd>

<dt><a name="#">I want to contribute a dataset or a survey catalog to the Microdata Library. What are the conditions?</a></dt> 
<dd>
<p>The Microdata Library is a DDI-based catalog (DDI is the international XML metadata standard we use to document and catalog datasets). A dataset will only be included in our catalog when we have DDI-compliant metadata. DDI metadata can be generated by data producers using a specialized DDI metadata editor, such as the free IHSN Microdata Manatgement Toolkit. In some cases, the microdata Library generates the DDI metadata. The catalog is for any type of microdata from low- and middle-income countries, for which metadata is available without restriction, and for which data can be accessed free of charge under clearly defined conditions (as public use files, licensed files, or possibly in a data enclave).
<ul class="bl">
	<li>We reserve the right to include or not datasets submitted to us.</li>
	<li>People submitting datasets must have the legal authority to do so.</li>
	<li>We do not pay providers of data.</li>
</ul>
</p> 
</dd>

<dt><a name="#">If I contribute datasets to the catalog, how will I be informed of the demand for my data?</a></dt> 
<dd>
<p>The Microdata Library application generates detailed information on visits (and more) to our websites. This information is shared with contributing repositories.</p>
</dd>

<dt id="tools"><a name="tools">Can I get help in implementing a catalog in my agency?</a></dt>
<dd>
<p>One of our key activities is technical support and capacity  building: supporting data producers in client countries and partner agencies in  adopting international good practices of curation and dissemination. </p>
<p>We do this by developing and providing free open source software solutions to the community as well by developing and providing  documentation and training on good practice for data curation and  dissemination.</p>
<p>Much of this work is conducted with the <a href="http://www.ihsn.org" target="_blank">International Household Survey Network (IHSN)</a> and its agencies as well as the <a href="http://www.ihsn.org/adp/" target="_blank">Accelerated  Data Program (ADP)</a>. These programs currently provide support activities in more than 70 countries and partner agencies through the ADP and IHSN.</p>	
</dd>

</dl>

