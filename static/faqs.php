<h1>Frequently Asked Questions</h1>
<p>&nbsp;</p>

<h2>For users of data</h2>

<dl>

<dt id="microdata"><a name="microdata">What do we mean by microdata?</a></dt>
<dd>
	<p>Microdata are unit-level data obtained from sample surveys, censuses, and administrative systems. They provide information about characteristics of individual people or entities such as households, business enterprises, facilities, farms or even geographical areas such as villages or towns. They allow in-depth understanding of socio-economic issues by studying relationships and interactions among phenomena. Microdata are thus key to designing projects and formulating policies, targeting interventions and monitoring and measuring the impact and results of projects, interventions and policies.</p>
</dd>


<dt id="improve"><a name="improve">How can I contribute to improving the catalog?</a></dt>
<dd>

<ul class="bl">
<p>You can:<p/>
	<li>Suggest improvements to the functionality of the catalog application we are using to power the Microdata Library. The application which drives our catalog is an open source application.</li> 
	<li>Inform us of problems found in any of the datasets listed in our catalog.</li> 
	<li>Send us any metadata and other materials you may have or may have generated while using a dataset you obtained from our catalog. Metadata is not always complete for all datasets, for example, variables and value labels may not be available for all datasets. If you create programs in Stata or SPSS to generate these labels and would like to share them with us then please send them to us.</li>
	<li>Send us citations of published work done using datasets in our catalog. When sending a citation which you would like us to include for a particular dataset please provide the full citation along with the title and reference number of the dataset in our catalog. You can also send the citation in BibTex or Endnote format to us.</li>
	<li>Inform us of datasets or catalogs that you think could be added to our catalog. We require that the DDI metadata be prepared and made available for the dataset (we can help with generating DDI's for datasets of particular interest). The data provided must be also free access. We make no guarantee that a dataset provided to us will be included in the catalog.</li>
</ul>
<p>Send your suggestions to microdata@worldbank.org</p>
</dd>

<dt><a name="#">I need a dataset which is not listed in your central catalog, but which has been used in World Bank publications. Can you help me obtain these data?</a></dt>
<dd>
<p>No.  The World Bank has access to datasets for specific and internal use. If the dataset is not in our catalog, it means that (i) we have no authorization to release these data, or (ii) the data and metadata are not ready for dissemination. We are working with data providers and we advocate more open access to data. We are supporting many countries to document and release datasets, and we are working on adding datasets regularly to our catalog. We hope to make datasets you are interested in available, but we do not have resources to support each and every data user in his/her quest for data.</p>
</dd>

<dt><a name="#">I am interested in knowing what are the existing research outputs associated with a dataset listed in your catalog. Do you maintain a bibliography of data-related citations?</a></dt>
<dd>
<p>This is a work in progress. We have started adding citations of publications using our datasets to the LSMS datasets. We expect to extend this to all other surveys. We are working with the International Household Survey network ( IHSN)  who is leading the establishment of a central citations catalog. When ready, this will be made accessible through the IHSN website and through our own catalogs.</p>
</dd>

<dt id="analyze"><a name="analyze">Can you help with analyzing the data?</a></dt>
<dd>
<p>No. We do not have the resources. Datasets are provided by many agencies and we do not have the staff with experience and expertise in each and every dataset. We make all metadata we have accessible to users. We are interested in feedback, but cannot provide technical support.</p>
</dd>

<dt><a name="#">I have used other survey catalogs which provide a search by topic and keyword. Why don't you provide such a tool?</a></dt>
<dd>
<p>This would indeed be a very useful feature. We have considered using a standardized multi-lingual topic classification and a keyword thesaurus. Implementing this on a systematic basis would require resources we currently do not have. Many of the datasets listed in our catalog are collected from external catalogs, implementing multi-lingual topic classifications and a keyword thesaurus would require all our contributing catalogs to do the same. This would be difficult to achieve and reduce the number of surveys we can include in our portal. Such a system may be considered in the future.</p>
</dd>

<dt><a name="#">What will you do with information I provide when I register, login, or submit a request for a licensed dataset?</a></dt>
<dd>
<p>What we do with registration and login information is governed by the terms of the World Bank's <a href="http://go.worldbank.org/FIKABS7U10" target="_blank">Privacy Policy</a>. Information you provide when requesting access to licensed data files will also be treated as restricted. It will be shared with the request review team, and may be shared with the data provider. But this information will not be made public.</p>
</dd>

<dt><a name="#">Can I be automatically notified of changes or additions to the Microdata Library catalog?</a></dt>
<dd>
<p>A "Notify me" option will be added soon.</p>
</dd>

<dt><a name="#">The catalog provides access to dataset in a specific file format. I would like to get the data in another format. Can you help?</a></dt>
<dd>
<p>In the catalogs maintained by the Microdata Library itself, data are provided in Stata and SPSS formats. If the demand justifies it, we may consider adding other formats.  Let us know about your preferences. Data provided by external catalogs are under their control. We will share with them feedback from users. We do not offer a service for data conversion. Software like StatTransfer, or the <a  target="_blank" href="http://www.nesstar.com/software/publisher.html">Nesstar Publisher</a> (which is freeware) can be used to convert datasets into other formats.</p>
</dd>

<dt><a name="#">I need a more comprehensive list of surveys and censuses conducted in low and middle-income countries. Where can I find that?</a></dt>
<dd>
<p>Our catalog is currently restricted to surveys for which microdata are made available to analysts. The surveys in our catalog are obtained from a network of contributing repositories. A more comprehensive list of surveys conducted in developing countries is available from the <a href="http://www.ihsn.org/home/index.php?q=activities/catalog/surveys"  target="_blank"> IHSN</a> Central Catalog.</p>
</dd>

</dl>

