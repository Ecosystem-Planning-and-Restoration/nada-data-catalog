<a name="searching"></a><h3>Searching in Study and or Variable Description
</h3>
<p>To search for keywords in the study description and display the studies containing those keywords, fill in your keywords in the first text box. For example: Title, Primary Investigator,Funder, Series, Study Abbreviation, would be relevant in this field.
</p>
<p><b>Important:</b> The system is only able to find keywords found in the metadata entered for a study. In cases where metadata is incomplete, such as where variable lables are missing in the data, results for that study may not be returned even though the particular topic you are searching for was covered in the study.  
Also note that many of the datasets are documented in languages other than English. In such cases searches conducted using English keywords will not return results from studies conducted in other languages. If you know a study was conducted in say, Spanish, then you should use Spanish keywords in your search.</p>
<ul class="bl">
<li>To search for keywords in the study variable description and display the studies containing those keywords, fill in your keywords in the second text box. For example: gender, labor, health, age, house, employed, weight, height, expenditure, income etc.</li>
<li>To limit this search in variable description to only the <em><strong>variable name</strong></em>, <em><strong>label</strong></em>, <em><strong>question</strong></em> or <em><strong>classification</strong></em><strong></strong>, tick one of the boxes below the second text box.</li>
<li>Initiate the search by clicking on the <img src="files/help-search-submit.png" alt="search"> button. To start a new search from the beginning click on the <img src="files/help-search-reset.png" alt=""> button.</li>
</ul>

