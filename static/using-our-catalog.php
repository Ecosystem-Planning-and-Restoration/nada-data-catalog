<p>Click on the bar labeled Countries in the Search dialog box to expand it.</p>
<p><img src="files/help-country-filter.png" alt="Country Filter" class="img-1"></p>
<ul class="bl">
<li>Select the countries you want to display in the catalog by clicking on the box\boxes next to your country\countries of interest. The filter is automatically applied as you click.</li>
<li>To select all countries click on <em><strong>select all</strong></em> in the top right of the Countries dialog box.</li>
<li>To clear your selection click on <em><strong>clear</strong></em> in the top right top right of the Countries dialog box.</li>
<li>To switch your selection from those countries selected to those not selected click on <em><strong>toggle</strong></em></li>
<li>The number of studies available in the catalog for each country is shown in brackets next to the country name.</li>
</ul>
