<a name="tips"></a><h3>Search Tips</h3>
<p>The Microdata Catalog uses Full-Text Search. This allows you to search the full text of all metadata for all the studies in the catalog.</p> 
<p>The full text includes all fields in:</p>

<ul class="bl">
<li>the study description - for example -  in study title, collection date, study producer, study abbreviation, study sponsors, keywords, etc.</li>
<li>the variable description - for example - in the variable name, variable label, variable value labels, variable literal question, etc.</li>  
</ul>  
  
<p></p>Full-Text Search uses "natural language" or Boolean search operators to retrieve the information you need.</p>
<p> Natural language searches are searches conducted using the language you would use in speaking to a person. For example:  <b>drinking water sources in rural areas.</b></p>
<p>Boolean searching allows you to narrow, broaden, or limit a search by connecting search terms with Boolean operators such as AND, OR, and NOT. <b>Note:</b> these must be written in CAPS. Some examples of how to conduct searches using Boolean operators are shown below :</p>


<a name="examples"></a><h3>Search Examples</h3>
<ul class="bl">
<li>Multiple keywords listed in a field separated by <strong>spaces</strong> will yield search results containing all studies that meet any of the typed criteria. A list of keywords will therefore be treated as an <strong>OR</strong> statement. For example: Listing <strong>gender age</strong> in the variable description will show all studies that have either gender or age in their variable description.<br><br></li>
<li>Placing <strong>AND</strong> between words treats the search as an <strong>AND</strong> statement. For example :<strong> Gender  AND Education AND Employed</strong> will limit results to studies that contain all the elements, Gender and Education and Employed in them.<br><br></li>
<li>Placing <strong>NOT</strong> between words treats the search as a<strong> NOT statement.</strong> For example:<strong> Albania AND Kenya NOT World</strong> will limit results to studies that contain all the elements Albania and Kenya, but exclude results for World from the results.<br><br></li>
<li>Placing quotation marks <strong>""</strong> around a search term will force the term to be evaluated as one term. For example: to limit a search to study descriptions that contain the exact sequence of words<em> health study</em> in them one would type <strong><em>"</em>Health Study<em>"</em> </strong>between quotation marks. Without the quotation marks the search would show all studies with health or study in them. With the quotation marks only studies with the exact wording in the sequence <em>health study</em> will be shown.<br><br></li>
<li>Substituting letters in words or phrases with a wildcard <strong>asterisk *</strong> is allowed. For example searching for heat<strong>*</strong> would yield results for heating, heated, heater.<br><br></li>
<li>The wildcard<strong> ? </strong>may also be used in searches to substitute for letters. For example: searching for <strong>employ?</strong> would yield results for employ or employed.<br><br></li>
</ul>



