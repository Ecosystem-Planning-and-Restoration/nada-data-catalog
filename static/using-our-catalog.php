<h1>Using our catalog</h1>
<p>The Microdata Catalog displays a list of all microdata available in the Microdata Library. The full collection of microdata in the catalog can be viewed from the DATASETS tab in the Microdata Catalog. The catalog has a number of features which allows the display of results to be limited to specific search criteria as well as a tool for comparing variables.</p>

